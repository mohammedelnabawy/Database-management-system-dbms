valid=1;
if [ $FieldType = "Int" ];
then
	if ! [[ $FieldValue =  +([0-9]) ]];
	then
		valid=0;
		echo "----------------------------------------------------------------------"
		echo "----------------------The value should be int-------------------------"
		echo "----------------------------------------------------------------------"
	fi
else
	if ! [[ $FieldValue = [A-Za-z]*([A-Z]|[_@-]|[a-z]|[0-9]) ]];
	then
		valid=0;
		echo "----------------------------------------------------------------------"
		echo "-----------The value should not contain Special characters------------"
		echo "----------------------------------------------------------------------"	
	fi
fi
